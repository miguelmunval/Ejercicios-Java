public class ejercicio4 {
    public static void main(String[] args) {
        int numero = 340;
        System.out.println("Este programa muestra los números del 320 al 160, contando de 20 en 20 hacia atrás utilizando un bucle for.");
        for (int i = 0; i < 9; i++) {
            numero-=20;
            System.out.println("El número actual es: "+numero);
        }
    }
}
